// Maps

// holds key-value pairs where the keys can be any datatype.
// remembers the original insertion order of the keys.
// has a property that represents the size of the map.
// Offer more functionality and functions compared to Object.
// Better for performance

// Declare with predefined values like this
const fruits = new Map([
  ["apples", 20],
  ["oranges", 50],
  ["strawberries", 10],
]);

// Or declare like this
const cars = new Map();
cars.set("Hyundai", 2015);
cars.set("Audi", 2023);
cars.set("Porsche", 2020);
