// type alias

// Type alias is a name that we can use to refer to a type. It does not create a new type, it just creates a new name to refer to that type.

// Example of when to use types:
// - You need to create custom type aliases for complex types, such as unions, intersections, or mapped types.
// - You want to define a type that represents a specific combination of other types, possibly with computed properties or conditional types.

type StringOrNumber = string | number;
